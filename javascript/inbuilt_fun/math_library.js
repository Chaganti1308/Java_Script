// math library provides to do lot of mathematical operations.

//abs
// Math.abs(-ve value) --> +ve value
// Math.abs(+ve value) --> +ve value
console.log(Math.abs(-9));
console.log(Math.abs(10));

// round_of
console.log(Math.round(4.69898));
console.log(Math.round(6.39898));
console.log(Math.round(10.55555));

// floor
// returns the first integer less than x

console.log(Math.floor(7.6)); // returns 6


// ceil
// returns the first integer greater than  x
console.log(Math.ceil(7.6)); // returns 8


// min and max
console.log(Math.max(5,7,9));
console.log(Math.min(5,7,9));

// sqrt - square root
console.log(Math.sqrt(9));