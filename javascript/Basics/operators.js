// +,-,/,*

const numberOne = "2"
const numberTwo = "4"

const numberThree = numberOne + numberTwo

console.log(numberThree);  // output : 24 -- string concatenation



const strOne = "Hello.."
const strTwo = "Neeha"

const strThree = strOne + strTwo

console.log(strThree);  // output : Hello..Neeha -- string concatenation

let x = 10
